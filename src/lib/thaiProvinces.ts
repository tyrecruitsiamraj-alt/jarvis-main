const PROVINCE_ALIAS_TO_CANON: Record<string, string> = {
  กรุงเทพ: 'กรุงเทพมหานคร',
  กรุงเทพฯ: 'กรุงเทพมหานคร',
  กทม: 'กรุงเทพมหานคร',
  'กทม.': 'กรุงเทพมหานคร',
};

const provinceSet = new Set(THAI_PROVINCE_NAMES);

/** เรียงชื่อจังหวัดสำหรับแสดงใน dropdown */
export const THAI_PROVINCE_NAMES_SORTED: readonly string[] = [...THAI_PROVINCE_NAMES].sort((a, b) =>
  a.localeCompare(b, 'th'),
);

export function canonProvinceName(raw: string): string | null {
  const t = raw.trim();
  if (!t) return null;
  if (PROVINCE_ALIAS_TO_CANON[t]) return PROVINCE_ALIAS_TO_CANON[t];
  if (provinceSet.has(t)) return t;
  return null;
}

export function isKnownThaiProvinceName(name: string): boolean {
  const c = canonProvinceName(name);
  return c != null && provinceSet.has(c);
}
